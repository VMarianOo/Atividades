package br.com.generation.variaveis;

public class Variavel02 {

	public static void main(String[] args) {
		
		int x = 10;
		int y = 5;
		int resultado;
		
		resultado = x + y;
		System.out.println("Soma: " + resultado);
		
		resultado = x - y;
		System.out.println("Soma: " + resultado);
		
		resultado = x * y;
		System.out.println("Soma: " + resultado);
		
		resultado = x / y;
		System.out.println("Soma: " + resultado);
		

	}

}
